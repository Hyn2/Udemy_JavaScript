// === 과 ==의 차이
// ===는 값의 일치와 데이터 타입 일치 여부까지 비교함
// 아래는 예제
var a = 1;
var b = '1';

if(a === b) {
    console.log('yes');
} else {
    console.log('no');
}

// 예상 출력 : no